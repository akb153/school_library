package beans;

public class HighSchoolBean {
	
	//社員属性(プロパティ)
	private String high_school_id;
	private String high_school_name;
	private String post_code;
	private String address;
	private String phone_number;
	private String opening_time;
	private String closing_time;
	public String getHigh_school_id() {
		return high_school_id;
	}
	public void setHigh_school_id(String high_school_id) {
		this.high_school_id = high_school_id;
	}
	public String getHigh_school_name() {
		return high_school_name;
	}
	public void setHigh_school_name(String high_school_name) {
		this.high_school_name = high_school_name;
	}
	public String getPost_code() {
		return post_code;
	}
	public void setPost_code(String post_code) {
		this.post_code = post_code;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getPhone_number() {
		return phone_number;
	}
	public void setPhone_number(String phone_number) {
		this.phone_number = phone_number;
	}
	public String getOpening_time() {
		return opening_time;
	}
	public void setOpening_time(String opening_time) {
		this.opening_time = opening_time;
	}
	public String getClosing_time() {
		return closing_time;
	}
	public void setClosing_time(String closing_time) {
		this.closing_time = closing_time;
	}
	
}